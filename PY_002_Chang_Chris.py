name = "This is a string text."
spcname = "	Spaced Text	"


print(f"title() output: {name.title()}")
print(f"upper() output: {name.upper()}")
print(f"lower() output: {name.lower()}")
print(f"fstring output: {name} *")
print(f"left strip output: *{spcname.lstrip()}*")
print(f"right strip output: *{spcname.rstrip()}*")
print(f"strip output: *{spcname.strip()}*")
print(f"tabs output: \t\t\t {name}")
print(f"new line output: \n{name}")



